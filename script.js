function toggleLang() {
    // Close all panels first, then open the one nearest to the click
    document.querySelectorAll('.lang-panel').forEach(p => p.classList.toggle('open'));
}

window.addEventListener('click', (e) => {
    if (!e.target.closest('.lang-wrap')) {
        document.querySelectorAll('.lang-panel').forEach(p => p.classList.remove('open'));
    }
});

function setLanguage(lang) {
    document.querySelectorAll('.lang-panel').forEach(p => p.classList.remove('open'));

    // Update flag icons (desktop + mobile)
    ['flag-icon', 'flag-icon-m'].forEach(id => {
        const el = document.getElementById(id);
        if (el) el.src = FLAGS[lang];
    });
    ['lang-label', 'lang-label-m'].forEach(id => {
        const el = document.getElementById(id);
        if (el) el.innerText = lang.toUpperCase();
    });

    // Translate text
    document.querySelectorAll('[data-key]').forEach(el => {
        const key = el.getAttribute('data-key');
        if (T[lang] && T[lang][key]) {
            // Preserve inner icons
            const icon = el.querySelector('i');
            if (icon) {
                const iconHTML = icon.outerHTML;
                el.innerHTML = iconHTML + ' ' + T[lang][key];
            } else {
                el.innerText = T[lang][key];
            }
        }
    });

    // Translate placeholders
    document.querySelectorAll('[data-key-placeholder]').forEach(el => {
        const key = el.getAttribute('data-key-placeholder');
        if (T[lang] && T[lang][key]) el.placeholder = T[lang][key];
    });

    // Update About Us Image dynamically based on language
    const aboutImg = document.getElementById('about-us-img');
    if (aboutImg) {
        if (lang === 'en') {
            aboutImg.src = "534645404_17880928584365437_5614943620985616051_n.jpg";
        } else {
            // For ku and ar
            aboutImg.src = "535047478_17880774534365437_7911296261637957477_n.jpg";
        }
    }

    // RTL handling
    document.body.classList.toggle('rtl', lang === 'ar' || lang === 'ku');
    localStorage.setItem('lang', lang);
}

