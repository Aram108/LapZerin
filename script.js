function openProductModal(title, price, imgSrc) {
    const modal = document.getElementById('product-overview-modal');
    if (!modal) return;

    document.getElementById('pom-title').innerText = title;
    document.getElementById('pom-price').innerText = '$' + price.toFixed(2);
    document.getElementById('pom-img').src = imgSrc;

    modal.classList.add('open');
    document.body.style.overflow = 'hidden';

    // GSAP Slide Animation for Image
    if (typeof gsap !== 'undefined') {
        gsap.fromTo('#pom-img', { x: -50, opacity: 0 }, { x: 0, opacity: 1, duration: 0.5, ease: "power2.out" });
        gsap.fromTo('.pom-content', { x: 50, opacity: 0 }, { x: 0, opacity: 1, duration: 0.5, ease: "power2.out", delay: 0.2 });
    }
}

function closeProductModal() {
    const modal = document.getElementById('product-overview-modal');
    if (modal) {
        modal.classList.remove('open');
        document.body.style.overflow = '';
    }
}

// Global click listener for product images
document.addEventListener('click', (e) => {
    const imgWrapper = e.target.closest('.prod-img');
    if (imgWrapper) {
        const card = imgWrapper.closest('.product-card');
        if (card) {
            const titleEl = card.querySelector('.prod-body h3');
            const priceEl = card.querySelector('.prod-price');
            const imgEl = card.querySelector('.prod-img img');
            if (titleEl && priceEl && imgEl) {
                const title = titleEl.innerText;
                const price = parseFloat(priceEl.innerText.replace('$', ''));
                openProductModal(title, price, imgEl.src);
            }
        }
    }
});

/* ══════════════════════════════════════
   SHOPPING CART & CHECKOUT PAGE
   ══════════════════════════════════════ */
let cart = JSON.parse(localStorage.getItem('lapzerin_cart')) || [];

function saveCart() {
    localStorage.setItem('lapzerin_cart', JSON.stringify(cart));
}

function addToCart(productName, price) {
    const existingIndex = cart.findIndex(item => item.name === productName);
    if (existingIndex > -1) {
        cart[existingIndex].qty += 1;
    } else {
        cart.push({ name: productName, price: price, qty: 1 });
    }
    saveCart();
    updateCartBadge();

    // Tiny pop animation for badge
    const badge = document.getElementById('cart-count-badge');
    if (badge) {
        badge.style.transform = 'scale(1.5)';
        setTimeout(() => badge.style.transform = 'scale(1)', 300);
    }
}

function updateCartBadge() {
    const counts = document.querySelectorAll('.cart-badge');
    const totalQty = cart.reduce((sum, item) => sum + item.qty, 0);
    counts.forEach(el => el.innerText = totalQty);
}

function renderCartPage() {
    const container = document.getElementById('cart-page-items');
    if (!container) return; // not on cart page

    const lang = localStorage.getItem('lang') || 'en';

    if (cart.length === 0) {
        container.innerHTML = `<p style="color:var(--white-60); text-align:center; padding: 30px;">${T[lang]?.empty_cart || "Your cart is empty!"}</p>`;
        document.getElementById('co-subtotal').innerText = '$0.00';
        document.getElementById('co-tax').innerText = '$0.00';
        document.getElementById('co-delivery').innerText = '$0.00';
        document.getElementById('co-total').innerText = '$0.00';
        return;
    }

    container.innerHTML = '';
    let subtotal = 0;

    cart.forEach((item, index) => {
        let itemTotal = item.price * item.qty;
        subtotal += itemTotal;

        let row = document.createElement('div');
        row.className = 'cart-page-item';
        // Add a delete button to item
        row.innerHTML = `
            <div style="flex:1">
                <h4 style="font-size: 1.1rem;">${item.name}</h4>
                <p style="color:var(--white-60); font-size:0.9rem;">$${item.price.toFixed(2)} each</p>
            </div>
            <div style="display:flex; align-items:center; gap:15px;">
                <strong>${item.qty}x</strong>
                <strong style="color:var(--gold);">$${itemTotal.toFixed(2)}</strong>
                <button onclick="removeFromCart(${index})" style="background:none; border:none; color:var(--white-30); cursor:pointer;"><i class="fas fa-trash-alt"></i></button>
            </div>
        `;
        container.appendChild(row);
    });

    const tax = subtotal * 0.05;
    const delivery = 5.00;
    const total = subtotal + tax + delivery;

    document.getElementById('co-subtotal').innerText = '$' + subtotal.toFixed(2);
    document.getElementById('co-tax').innerText = '$' + tax.toFixed(2);
    document.getElementById('co-delivery').innerText = '$' + delivery.toFixed(2);
    document.getElementById('co-total').innerText = '$' + total.toFixed(2);
}

function removeFromCart(index) {
    cart.splice(index, 1);
    saveCart();
    updateCartBadge();
    renderCartPage();
}

function submitOrder() {
    if (cart.length === 0) {
        let lang = localStorage.getItem('lang') || 'en';
        alert(T[lang]?.empty_cart || "Your cart is empty!");
        return;
    }
    alert("Order successful! A taxi driver will deliver it to you soon.");
    cart = [];
    saveCart();
    updateCartBadge();
    renderCartPage();
    // Redirect to home
    setTimeout(() => { window.location.href = "index.html"; }, 1500);
}


